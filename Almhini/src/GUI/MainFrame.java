    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        if(newItem != null){
            newItem.dispose();
        }
        if(editProduct != null){
            if(editProduct.search != null){
                editProduct.search.dispose();
            }
            editProduct.dispose();
        }
        if(sells != null){
            sells.dispose();
        }
        if(editBill != null){
            editBill.dispose();
        }
        if(stat != null){
            stat.dispose();
        }
        
        try {
            mysqlStop = Runtime.getRuntime().exec("C:\\xampp\\mysql\\bin\\mysqladmin.exe -u root stop");
            mysqlStart.destroy();
            mysqlStop.destroy();
            db.close();
        } catch (Exception ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowClosing

