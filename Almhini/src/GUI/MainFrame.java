    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
        
        String sql = "select * from bills";
        ResultSet rs = db.select(sql);
        boolean state = false;
        while(rs.next()){
            state = true;
        }
        if(state){
            if(editBill == null){
                editBill = new EditBill();
                editBill.setResizable(false);
                editBill.setVisible(true);
            }
            else{
                editBill.toFront();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "لم يتم انشاء أي فاتورة الى حد الان حتى يتم تعديلها أو الغائها");
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "لم يتم انشاء أي فاتورة الى حد الان حتى يتم تعديلها أو الغائها");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

