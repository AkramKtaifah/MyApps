    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            
                ResultSet rs = db.getNameOfTables();
                while(rs.next()){
                    if(!rs.getString(3).equals("bills") & !rs.getString(3).equals("sells")){
                        nameOfTables.add(rs.getString(3));
                    }
                }
                
                if(nameOfTables.isEmpty()){
                    JOptionPane.showMessageDialog(null, "لم تتم اضافة اي صنف الى حد الان حتى يتم التعديل على عناصره");
                }
                else{
                    if(editProduct == null){
                        editProduct = new EditProduct();
                        editProduct.setResizable(false);
                        editProduct.setVisible(true);
                    }
                    else{
                        editProduct.toFront();
                        if(editProduct.search != null){
                            editProduct.search.toFront();
                        }
                    }
                }
            
        } catch (Exception ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

